    public void setVisible(boolean visible) {
        if (!super.isVisible()) {
            try {
                String[] config = crtlApp.ObtenerConfiguracion();
                /*0  clasificador = "J48";
                1  rutaGrabacionFicheros = "C:/ProyectoFinalCarreraDani/";
                2  pesoDistIzquierda = 0;
                3  pesoDistDerecha = 0;
                4  escenarioPredifinido = false;
                5  relentizacion = 0;    */
                if (config[0].equalsIgnoreCase("bayesnet")) {
                    chkBayesNet.setSelected(true);
                } else {
                    if (config[0].equalsIgnoreCase("naivebayes")) {
                        cHKNaiveBayes.setSelected(true);
                    } else {
                        if (config[0].equalsIgnoreCase("j48")) {
                            cHKJ48.setSelected(true);
                        } else {
                            if (config[0].equalsIgnoreCase("j48graft")) {
                                cHKJ48Graft.setSelected(true);
                            } else {
                                if (config[0].equalsIgnoreCase("nbtree")) {
                                    cHKNBTree.setSelected(true);
                                } else {
                                    if (config[0].equalsIgnoreCase("bayesnetTAN")) {
                                        ChkBayedNetTAN.setSelected(true);
                                    }
                                }
                            }
                        }
                    }
                }
                txtRutaFicheros.setText(config[1]);
                sIzquierda.setValue((int) (Double.parseDouble(config[2]) * 100));

                sDerecha.setValue((int) (Double.parseDouble(config[3]) * 100));

                lblIzquierda.setText("" + config[2]);
                lblDerecha.setText("" + config[3]);

                if (Boolean.parseBoolean(config[4])) {
                    ChkUsarEscenaroPredefinido.setSelected(true);
                } else {
                    chkUsarEscenarioAleatorio.setSelected(true);
                }


                if (config[5].equals("0")) {
                    chkActivarRelentizacion.setSelected(false);
                    DesactivaSeleccionRelentizacion();
                } else {
                    chkActivarRelentizacion.setSelected(true);
                    ActivaSeleccionRelentizacion();
                }
                txtRiempoRelentizacion.setText(config[5]);

                txtTiempoGrabacion.setText("" + config[6]);

                txtTiempoGrabacionEstadisticas.setText(config[7]);
                txtSeed.setText(config[8]);

                 if (Boolean.parseBoolean(config[9])) {
                    ChkCrossValidation.setSelected(true);
                } else {
                    ChkCrossValidation.setSelected(false);
                }

            } catch (Exception e) {
                JDExcepcion jDExcepcion = new JDExcepcion();
                jDExcepcion.setPreferredSize(new Dimension(564, 230));
                jDExcepcion.pack();
                jDExcepcion.setLocationRelativeTo(this);
                jDExcepcion.setText(e.getMessage());
                jDExcepcion.setVisible(true);
            }


        }
        super.setVisible(visible);
    }


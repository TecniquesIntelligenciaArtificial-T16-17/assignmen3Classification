private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
// TODO add your handling code here:
    //Grabar
    String[] config = new String[10];
    if (chkBayesNet.isSelected()) {
        config[0] = "bayesnet";
    } else {
        if (cHKNaiveBayes.isSelected()) {
            config[0] = "naivebayes";
        } else {
            if (cHKJ48.isSelected()) {
                config[0] = "j48";
            } else {
                if (cHKJ48Graft.isSelected()) {
                    config[0] = "j48graft";
                } else {
                    if (cHKNBTree.isSelected()) {
                        config[0] = "nbtree";
                    } else {
                        if (ChkBayedNetTAN.isSelected()) {
                            config[0] = "bayesnetTAN";
                        }
                    }
                }
            }
        }
    }

    config[1] = txtRutaFicheros.getText();
    config[2] = "" + sIzquierda.getValue();
    config[3] = "" + sDerecha.getValue();
    config[4] = "" + ChkUsarEscenaroPredefinido.isSelected();
    config[5] = "" + txtRiempoRelentizacion.getText();
    config[6] = "" + txtTiempoGrabacion.getText();
    config[7] = "" + txtTiempoGrabacionEstadisticas.getText();
    config[8] = "" + txtSeed.getText();
    config[9] = "" + ChkCrossValidation.isSelected();
    try {
        crtlApp.grabarConfiguracion(config);
        this.setVisible(false);
    } catch (Exception e) {
        JDExcepcion jDExcepcion = new JDExcepcion();
        jDExcepcion.setPreferredSize(new Dimension(564, 230));
        jDExcepcion.pack();
        jDExcepcion.setLocationRelativeTo(this);
        jDExcepcion.setText(e.getMessage());
        jDExcepcion.setVisible(true);
    }

}//GEN-LAST:event_jBAceptarActionPerformed
